function skivvy_show_commentmenu() {
	echo '<style>#menu-comments{ display:block !important; }</style>';
} add_action('admin_head', 'skivvy_show_commentmenu');

function skivvyavatar ($avatar_defaults) {
	$myavatar = get_bloginfo('template_directory') . '/img/avatar.gif';
	$avatar_defaults[$myavatar] = "WPBeginner";
	return $avatar_defaults;
} add_filter( 'avatar_defaults', 'skivvyavatar' );

